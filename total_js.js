//programação do total

function total_arroz(){
const preco_arroz = 20;
let valor_arroz = document.getElementById("quantidade_arroz").value * preco_arroz;
if(document.getElementById("total").innerHTML == ""){
console.log("certo");
//
document.getElementById("total").innerHTML = parseFloat(valor_arroz.toFixed(2));
}
else{
console.log("ainda não");
//
document.getElementById("total").innerHTML = parseFloat(document.getElementById("total").innerHTML) + parseFloat(valor_arroz.toFixed(2));
}
}

